// Helper function to find a tool by category and tool slug
export function findTool(categorySlug, toolSlug) {
  for (const category of toolCategories) {
    for (const tool of category.items) {
      if (tool.categorySlug === categorySlug && tool.slug === toolSlug) {
        return tool;
      }
    }
  }
  return null;
}
